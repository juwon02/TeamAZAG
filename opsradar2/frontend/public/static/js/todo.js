var todoState = window.todoState || { currentTodoTab: 'ai', selectedTodoId: null, todoChecked: {}, editTargetId: null, reviewFilter: null };
window.todoState = todoState;

// Migrated from legacy app.js by scripts/import-legacy.mjs.
function approveTodo(id){
  const t=todos.find(x=>x.id===id);
  t.status='approved';
  showToast(`✅ "${t.title.slice(0,20)}..." 승인되었습니다.`,'success');
  addNotif(`"${t.title.slice(0,24)}..." Todo가 승인되었습니다.`, 'success');
  afterTodoAction(id);
}

function rejectTodo(id){const t=todos.find(x=>x.id===id);t.status='rejected';showToast('반려 처리되었습니다.','warn');afterTodoAction(id);}

function doneTodo(id){const t=todos.find(x=>x.id===id);t.status='done';showToast(`🎉 완료 처리되었습니다.`,'success');afterTodoAction(id);}

function undoTodo(id){const t=todos.find(x=>x.id===id);t.status='pending';showToast('승인 대기로 되돌렸습니다.','info');afterTodoAction(id);}

function afterTodoAction(id){renderTodos();if(todoState.selectedTodoId===id)renderTodoDetail(id);updateTodoCounts();}

function bulkApprove(){const checkedIds=Object.keys(todoState.todoChecked).filter(id=>todoState.todoChecked[id]).map(Number);let p=todos.filter(t=>t.status==='pending' && checkedIds.includes(t.id));const selectedMode=p.length>0;if(!p.length)p=todos.filter(t=>t.status==='pending');if(!p.length){showToast('승인 대기 항목이 없습니다.','info');return;}p.forEach(t=>{t.status='approved';todoState.todoChecked[t.id]=false;});const chkAll=document.getElementById('chkAll');if(chkAll)chkAll.checked=false;showToast(`✅ ${p.length}건 ${selectedMode?'선택 승인':'전체 승인'}되었습니다.`,'success');renderTodos();updateTodoCounts();}

function openEditModal(id){todoState.editTargetId=id;const t=todos.find(x=>x.id===id);document.getElementById('editTitle').value=t.title;document.getElementById('editAssignee').value=t.assignee||'담당자 A';document.getElementById('editModal').classList.add('show');}

function saveEdit(){const t=todos.find(x=>x.id===todoState.editTargetId);t.title=document.getElementById('editTitle').value.trim();t.assignee=document.getElementById('editAssignee').value;closeModal('editModal');renderTodos();if(todoState.selectedTodoId===todoState.editTargetId)renderTodoDetail(todoState.editTargetId);showToast('수정되었습니다.','info');}

function openManualModal(){document.getElementById('manualModal').classList.add('show');}

async function saveManual(){
  const title=document.getElementById('manualTitle').value.trim();
  if(!title)return;
  if(!window.opsRadarApi){showToast('DB 연결을 준비 중입니다. 잠시 후 다시 시도해주세요.','warn');return;}
  try{
    await window.opsRadarApi.request('/todos',{
      method:'POST',
      body:JSON.stringify({
        title,
        assignee:document.getElementById('manualAssignee').value||null,
        priority:document.getElementById('manualPriority').value||'medium',
        source:'manual',
        status:'pending'
      })
    });
    closeModal('manualModal');
    document.getElementById('manualTitle').value='';
    if(todoState.currentTodoTab!=='ai')switchTodoTab('ai');
    await window.opsRadarApi.loadTodos();
    showToast('Todo가 DB에 등록되었습니다.','success');
  }catch(error){
    console.warn('Todo create API failed',error);
    showToast('Todo 등록에 실패했습니다.','warn');
  }
}

